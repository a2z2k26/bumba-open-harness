/**
 * Generate prompts for coding agents
 * @param {string} exportPath - Path to export directory
 * @param {Object} bumbaContext - Bumba context
 */
function generatePrompts(exportPath, bumbaContext) {
  const promptsPath = path.join(exportPath, 'prompts');
  fs.mkdirSync(promptsPath, { recursive: true });

  // One-shot prompt
  const oneShotPrompt = `# One-Shot Implementation Prompt

I need you to implement a ${bumbaContext.framework} application based on the complete product specification package provided.

## Context

All product specifications are in the \`specifications/\` directory. Design assets (tokens and components) are in the \`.design/\` directory of the project.

**Framework**: ${bumbaContext.framework}
**Design Tokens**: ${bumbaContext.hasTokens ? 'Available in .design/tokens/' : 'Need to be extracted from Figma'}
**Components**: ${bumbaContext.hasComponents ? 'Available in .design/components/' : 'Need to be designed and extracted'}

## Your Task

1. Review all specifications in \`specifications/\`
2. Import design tokens from \`.design/tokens/\`
3. Use components from \`.design/extracted-code/${bumbaContext.framework}/\`
4. Implement all sections according to their specifications
5. Follow the data model in \`specifications/data-model/\`
6. Use sample data and types from each section

## Before You Start

Please ask me clarifying questions about:
- Authentication approach (JWT, session-based, OAuth, etc.)
- User modeling (roles, permissions, profile structure)
- Tech stack preferences (state management, routing, API client, etc.)
- Testing framework preferences
- Any ambiguities in the specifications

## Implementation Guidelines

- Follow the specifications exactly
- Use the design tokens for all styling
- Reference Bumba components where applicable
- Include error handling and validation
- Write tests according to \`tests.md\` in each section
- Create a clean, production-ready codebase

Let me know when you're ready to begin, and I'll answer your clarifying questions.
`;

  fs.writeFileSync(path.join(promptsPath, 'one-shot-prompt.md'), oneShotPrompt, 'utf-8');

  // Section prompt template
  const sectionPrompt = `# Section Implementation Prompt Template

I need you to implement the **[SECTION_NAME]** section of the application.

## Context

- **Framework**: ${bumbaContext.framework}
- **Specification**: See \`specifications/sections/[section-id]/spec.md\`
- **Sample Data**: See \`specifications/sections/[section-id]/data.json\`
- **Types**: See \`specifications/sections/[section-id]/types.ts\`

## Design Assets

- **Tokens**: \`.design/tokens/\` (use for colors, typography, spacing)
- **Components**: \`.design/extracted-code/${bumbaContext.framework}/\` (reference where applicable)

## Your Task

1. Review the section specification
2. Understand the user flows and UI requirements
3. Implement all screens/views for this section
4. Use the provided sample data and types
5. Follow the testing requirements in the specification

## Before You Start

Ask clarifying questions about:
- How this section integrates with other sections
- Any ambiguities in the specification
- State management approach for this section

Ready when you are!
`;

  fs.writeFileSync(path.join(promptsPath, 'section-prompt.md'), sectionPrompt, 'utf-8');
}

