/**
 * Export Builder - Build export package for implementation handoff
 *
 * Generates a complete export package with prompts, instructions, and specifications
 * that reference Bumba design assets for implementation by coding agents.
 */

const fs = require('fs');
const path = require('path');
const { getBumbaContext } = require('./bumba-reader');

/**
 * Copy directory recursively
 * @param {string} src - Source directory
 * @param {string} dest - Destination directory
 */
function copyDirRecursive(src, dest) {
  fs.mkdirSync(dest, { recursive: true });
  const entries = fs.readdirSync(src, { withFileTypes: true });

  for (const entry of entries) {
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);

    if (entry.isDirectory()) {
      copyDirRecursive(srcPath, destPath);
    } else {
      fs.copyFileSync(srcPath, destPath);
    }
  }
}

