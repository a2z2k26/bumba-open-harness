/**
 * Copy all specifications to export package
 * @param {string} productPath - Path to product directory
 * @param {string} exportPath - Path to export directory
 */
function copySpecifications(productPath, exportPath) {
  const specificationsPath = path.join(exportPath, 'specifications');

  if (fs.existsSync(productPath)) {
    copyDirRecursive(productPath, specificationsPath);
    console.log(`[export-builder] Copied specifications from ${productPath}`);
  }
}

/**
 * Build complete export package
 * @returns {string} - Path to export directory
 */
function buildExportPackage() {
  const bumbaContext = getBumbaContext();
  const productPath = path.resolve(__dirname, '../product');
  const exportPath = path.resolve(__dirname, '../design-direction-plan');

  // Create export structure
  fs.mkdirSync(exportPath, { recursive: true });

  // Generate files
  generateExportREADME(exportPath, bumbaContext);
  generatePrompts(exportPath, bumbaContext);
  generateInstructions(exportPath, bumbaContext);
  copySpecifications(productPath, exportPath);

  console.log(`[export-builder] Export package created at ${exportPath}`);
  return exportPath;
}

module.exports = {
  buildExportPackage,
  generateExportREADME,
  generatePrompts,
  generateInstructions,
  copySpecifications
};
