#!/usr/bin/env node
/**
 * Phase 6 Sprint 6.8: Integration Test - User Modification Preservation
 *
 * Tests that user modifications to generated code are properly detected
 * and preserved during cascade sync operations.
 */

const path = require('path');
const fs = require('fs');
const os = require('os');

// Test results tracking
const results = { passed: 0, failed: 0, tests: [] };

function check(name, condition) {
  if (condition) {
    results.passed++;
    results.tests.push({ name, status: 'passed' });
    console.log(`  ✅ ${name}`);
  } else {
    results.failed++;
    results.tests.push({ name, status: 'failed' });
    console.log(`  ❌ ${name}`);
  }
}

function createTestDirectory() {
  const testDir = path.join(os.tmpdir(), `user-mod-test-${Date.now()}`);
  fs.mkdirSync(path.join(testDir, '.design', 'extracted-code', 'react'), { recursive: true });
  fs.mkdirSync(path.join(testDir, '.design', 'stories'), { recursive: true });
  return testDir;
}

function cleanupTestDirectory(testDir) {
  try {
    fs.rmSync(testDir, { recursive: true, force: true });
  } catch (e) {}
}

console.log('\n=== Sprint 6.8: User Modification Preservation Test ===\n');

async function testUserModificationPreservation() {
  const { SyncCascade } = require('./sync-cascade');
  const { TransformStateUpdater } = require('./transform-state-updater');
  const { StoryHashRegistry } = require('./story-hash-registry');
  const { hashFile, hasFileChanged } = require('./content-hasher');
  const { readComponentRegistry, writeComponentRegistry, invalidateCache } = require('./registry-reader');

  const testDir = createTestDirectory();
  console.log(`Test directory: ${testDir}\n`);

  try {
    // 1. Set up: Create transformed component with known hash
    console.log('1. Set Up: Create Transformed Component');

    // Create code file
    const codePath = '.design/extracted-code/react/ModButton.tsx';
    const fullCodePath = path.join(testDir, codePath);
    const originalCode = `// Generated by Design Bridge
export const ModButton = ({ label }) => (
  <button className="btn">{label}</button>
);`;
    fs.writeFileSync(fullCodePath, originalCode);
    const originalHash = await hashFile(fullCodePath);

    // Create story file
    const storyPath = '.design/stories/ModButton.stories.tsx';
    const fullStoryPath = path.join(testDir, storyPath);
    const originalStory = `// Generated by Design Bridge
import { ModButton } from '../extracted-code/react/ModButton';
export default { title: 'ModButton' };
export const Default = () => <ModButton label="Click" />;`;
    fs.writeFileSync(fullStoryPath, originalStory);

    // Create registry with transformed component
    const registry = {
      version: '3.0.0',
      components: {
        'mod-button-001': {
          id: 'mod-button-001',
          name: 'ModButton',
          transformation: {
            state: 'transformed',
            framework: 'react',
            codePath,
            storyPath,
            codeHash: originalHash,
            transformedAt: new Date().toISOString()
          },
          syncMetadata: {
            lastFigmaSync: new Date().toISOString(),
            syncCount: 1
          }
        }
      }
    };
    await writeComponentRegistry(testDir, registry);

    // Register story in StoryHashRegistry
    const storyRegistry = new StoryHashRegistry(testDir);
    await storyRegistry.registerStory(storyPath, originalStory, { framework: 'react' });

    check('Component and files created', true);
    check('Original code hash stored', originalHash !== null);

    // 2. Test hasFileChanged detects no change initially
    console.log('\n2. Verify No Change Detection Initially');
    const initialCheck = await hasFileChanged(fullCodePath, originalHash);
    check('No change detected initially', initialCheck === false);

    // 3. Simulate user modification to code
    console.log('\n3. Simulate User Code Modification');
    const modifiedCode = `// Generated by Design Bridge
// USER MODIFICATION: Added custom styling
export const ModButton = ({ label, variant = 'primary' }) => (
  <button className={\`btn btn-\${variant}\`}>{label}</button>
);`;
    fs.writeFileSync(fullCodePath, modifiedCode);

    const afterModCheck = await hasFileChanged(fullCodePath, originalHash);
    check('Modification detected via hash', afterModCheck === true);

    // 4. Test needsRetransform respects user modifications
    console.log('\n4. needsRetransform Respects User Modifications');
    const updater = new TransformStateUpdater({ projectPath: testDir });
    const retransformCheck = await updater.needsRetransform('mod-button-001');

    check('needsRetransform returns object', typeof retransformCheck === 'object');
    check('Does NOT need retransform (preserve user changes)', retransformCheck.needs === false);
    check('userModified flag is true', retransformCheck.userModified === true);
    check('Reason indicates user modification', retransformCheck.reason === 'User modified code');

    // 5. Test SyncCascade shouldRegenerateCode respects user modifications
    console.log('\n5. SyncCascade Respects User Code Modifications');
    const cascade = new SyncCascade({ projectPath: testDir });
    const codeRegenCheck = await cascade.shouldRegenerateCode('mod-button-001');

    check('shouldRegenerateCode returns object', typeof codeRegenCheck === 'object');
    check('Should NOT regenerate code', codeRegenCheck.should === false);
    check('userModified flag present', codeRegenCheck.userModified === true);
    check('Action is preserve', codeRegenCheck.action === 'preserve');

    // 6. Simulate user modification to story
    console.log('\n6. Simulate User Story Modification');
    const modifiedStory = `// Generated by Design Bridge
// USER MODIFICATION: Added more examples
import { ModButton } from '../extracted-code/react/ModButton';
export default { title: 'Components/ModButton' };
export const Default = () => <ModButton label="Click" />;
export const Primary = () => <ModButton label="Primary" variant="primary" />;
export const Secondary = () => <ModButton label="Secondary" variant="secondary" />;`;
    fs.writeFileSync(fullStoryPath, modifiedStory);

    // 7. Test SyncCascade shouldRegenerateStory respects user modifications
    console.log('\n7. SyncCascade Respects User Story Modifications');
    const storyRegenCheck = await cascade.shouldRegenerateStory('mod-button-001');

    check('shouldRegenerateStory returns object', typeof storyRegenCheck === 'object');
    // Note: If story file modified, should NOT regenerate
    check('Should NOT regenerate story (preserve user changes)',
      storyRegenCheck.should === false || storyRegenCheck.userModified === true);

    // 8. Test cascade emits warning for preserved modifications
    console.log('\n8. Cascade Emits Warning for Preserved Code');
    let warningEmitted = false;
    let warningType = null;

    cascade.on('cascade:warning', (data) => {
      warningEmitted = true;
      warningType = data.type;
    });

    // Update syncMetadata to trigger source updated check
    invalidateCache();
    const reg = await readComponentRegistry(testDir, { forceRefresh: true });
    reg.components['mod-button-001'].syncMetadata.lastFigmaSync = new Date().toISOString();
    await writeComponentRegistry(testDir, reg);

    await cascade.cascade('mod-button-001', {});

    // Warning should be emitted for preserved user modifications
    check('Warning emitted for preserved modifications', warningEmitted === true);
    check('Warning type is code_preserved or story_preserved',
      warningType === 'code_preserved' || warningType === 'story_preserved');

    // 9. Verify original user modifications preserved after cascade
    console.log('\n9. Verify User Modifications Preserved After Cascade');
    const finalCode = fs.readFileSync(fullCodePath, 'utf8');
    check('User code modifications preserved', finalCode.includes('USER MODIFICATION'));
    check('User code changes intact', finalCode.includes('variant'));

  } finally {
    cleanupTestDirectory(testDir);
  }

  console.log('\n' + '='.repeat(50));
  console.log('USER MODIFICATION PRESERVATION TEST RESULTS');
  console.log('='.repeat(50));
  console.log(`\n  Total:  ${results.passed + results.failed}`);
  console.log(`  Passed: ${results.passed} ✅`);
  console.log(`  Failed: ${results.failed} ❌`);

  if (results.failed > 0) {
    process.exit(1);
  }
  console.log('\n✅ User modification preservation test passed!\n');
}

testUserModificationPreservation().catch(err => {
  console.error('Test error:', err);
  process.exit(1);
});
