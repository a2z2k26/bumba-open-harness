class DocParser {
  constructor() {
    this.patterns = {
      jsdoc: /\/\*\*[\s\S]*?\*\//g,
      tag: /@(\w+)\s*(?:\{([^}]*)\})?\s*([\s\S]*?)(?=@|\*\/)/g,
      param: /@param\s*(?:\{([^}]*)\})?\s*(\[?[\w.]+\]?)\s*-?\s*(.*)/,
      returns: /@returns?\s*(?:\{([^}]*)\})?\s*(.*)/,
      example: /@example\s*([\s\S]*?)(?=@|\*\/)/,
      typedef: /@typedef\s*(?:\{([^}]*)\})?\s*(\w+)/,
      property: /@property\s*(?:\{([^}]*)\})?\s*(\[?[\w.]+\]?)\s*-?\s*(.*)/
    };
  }

  parseFile(content) {
    const docs = [];
    const matches = content.matchAll(this.patterns.jsdoc);

    for (const match of matches) {
      const doc = this.parseBlock(match[0]);
      if (doc) {
        // Get the code following the doc block
        const afterDoc = content.slice(match.index + match[0].length);
        doc.code = this.extractFollowingCode(afterDoc);
        docs.push(doc);
      }
    }

    return docs;
  }

  parseBlock(block) {
    const doc = {
      description: '',
      params: [],
      returns: null,
      examples: [],
      tags: {}
    };

    // Extract description (text before first @tag)
    const descMatch = block.match(/\/\*\*\s*([\s\S]*?)(?=@|\*\/)/);
    if (descMatch) {
      doc.description = this.cleanText(descMatch[1]);
    }

    // Extract params
    const paramMatches = block.matchAll(/@param\s*(?:\{([^}]*)\})?\s*(\[?[\w.]+\]?)\s*-?\s*(.*)/g);
    for (const match of paramMatches) {
      const isOptional = match[2].startsWith('[');
      const name = match[2].replace(/[\[\]]/g, '').split('=')[0];
      const defaultValue = match[2].includes('=')
        ? match[2].match(/=([^\]]*)/)?.[1]
        : undefined;

      doc.params.push({
        name,
        type: match[1] || 'any',
        description: this.cleanText(match[3]),
        optional: isOptional,
        default: defaultValue
      });
    }

    // Extract returns
    const returnMatch = block.match(/@returns?\s*(?:\{([^}]*)\})?\s*(.*)/);
    if (returnMatch) {
      doc.returns = {
        type: returnMatch[1] || 'void',
        description: this.cleanText(returnMatch[2])
      };
    }

    // Extract examples
    const exampleMatches = block.matchAll(/@example\s*([\s\S]*?)(?=@|\*\/)/g);
    for (const match of exampleMatches) {
      doc.examples.push(this.cleanText(match[1]));
    }

    // Extract other tags
    const tagPatterns = ['throws', 'deprecated', 'since', 'see', 'author', 'version', 'private', 'public', 'protected', 'static', 'async'];
    for (const tag of tagPatterns) {
      const tagMatch = block.match(new RegExp(`@${tag}\\s*(?:\\{([^}]*)\\})?\\s*(.*)`, 'i'));
      if (tagMatch) {
        doc.tags[tag] = {
          type: tagMatch[1],
          value: this.cleanText(tagMatch[2])
        };
      }
    }

    return doc;
  }

  extractFollowingCode(content) {
    // Extract function/class/const declaration
    const patterns = [
      /^[\s\n]*(export\s+)?(async\s+)?function\s+(\w+)/,
      /^[\s\n]*(export\s+)?class\s+(\w+)/,
      /^[\s\n]*(export\s+)?(const|let|var)\s+(\w+)/
    ];

    for (const pattern of patterns) {
      const match = content.match(pattern);
      if (match) {
        return {
          type: match[0].includes('function') ? 'function'
            : match[0].includes('class') ? 'class'
            : 'variable',
          name: match[match.length - 1],
          exported: match[0].includes('export')
        };
      }
    }

    return null;
  }

  cleanText(text) {
    return text
      .replace(/^\s*\*\s?/gm, '')
      .replace(/\s+/g, ' ')
      .trim();
  }
}

