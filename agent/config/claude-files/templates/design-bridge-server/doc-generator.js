class APIDocGenerator extends EventEmitter {
  constructor(options = {}) {
    super();
    this.options = {
      outputDir: options.outputDir || './docs/api',
      format: options.format || 'markdown',
      language: options.language || 'typescript',
      ...options
    };
    this.templateEngine = new TemplateEngine();
    this.parser = new DocParser();
  }

  async generateFromFile(filePath) {
    const content = await fs.readFile(filePath, 'utf8');
    return this.generateFromSource(content, path.basename(filePath));
  }

  async generateFromSource(source, moduleName = 'module') {
    const docs = this.parser.parseFile(source);
    const moduleDoc = this.organizeDocumentation(docs, moduleName);

    const template = DOC_TEMPLATES.api.markdown;
    const content = this.templateEngine.render(template, {
      description: this.options.description || `API documentation for ${moduleName}`,
      language: this.options.language,
      modules: [moduleDoc]
    });

    this.emit('doc:generated', { module: moduleName });

    return {
      filename: `${this.formatKebabCase(moduleName)}.md`,
      content,
      type: 'api-doc'
    };
  }

  organizeDocumentation(docs, moduleName) {
    const module = {
      name: moduleName,
      anchor: this.formatKebabCase(moduleName),
      description: '',
      classes: [],
      functions: [],
      constants: []
    };

    for (const doc of docs) {
      if (!doc.code) continue;

      if (doc.code.type === 'class') {
        module.classes.push(this.formatClass(doc));
      } else if (doc.code.type === 'function') {
        module.functions.push(this.formatFunction(doc));
      } else if (doc.code.type === 'variable') {
        module.constants.push(this.formatConstant(doc));
      }
    }

    return module;
  }

  formatClass(doc) {
    return {
      name: doc.code.name,
      description: doc.description,
      constructor: doc.params.length > 0 ? {
        signature: `new ${doc.code.name}(${doc.params.map(p => p.name).join(', ')})`,
        params: doc.params
      } : null,
      methods: [], // Would need deeper parsing
      properties: []
    };
  }

  formatFunction(doc) {
    const params = doc.params.map(p => `${p.name}: ${p.type}`).join(', ');
    const returnType = doc.returns ? doc.returns.type : 'void';

    return {
      name: doc.code.name,
      description: doc.description,
      signature: `function ${doc.code.name}(${params}): ${returnType}`,
      params: doc.params,
      returns: doc.returns,
      example: doc.examples[0]
    };
  }

  formatConstant(doc) {
    return {
      name: doc.code.name,
      type: doc.tags.type?.value || 'unknown',
      value: doc.tags.default?.value || 'N/A',
      description: doc.description
    };
  }

  formatKebabCase(name) {
    return name
      .replace(/[^a-zA-Z0-9]/g, '-')
      .toLowerCase()
      .replace(/-+/g, '-')
      .replace(/^-|-$/g, '');
  }
}

