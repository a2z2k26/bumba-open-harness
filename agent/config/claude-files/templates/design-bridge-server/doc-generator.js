class ComponentDocGenerator extends EventEmitter {
  constructor(options = {}) {
    super();
    this.options = {
      outputDir: options.outputDir || './docs/components',
      format: options.format || 'markdown',
      language: options.language || 'tsx',
      packageName: options.packageName || '@design-bridge/components',
      includeExamples: options.includeExamples !== false,
      includeAccessibility: options.includeAccessibility !== false,
      ...options
    };
    this.templateEngine = new TemplateEngine();
  }

  async generate(component) {
    const docData = this.prepareComponentData(component);
    const template = DOC_TEMPLATES.component[this.options.format === 'mdx' ? 'mdx' : 'markdown'];
    const content = this.templateEngine.render(template, docData);

    const filename = `${this.formatKebabCase(component.name)}.${this.options.format === 'mdx' ? 'mdx' : 'md'}`;

    this.emit('doc:generated', { component: component.name, filename });

    return {
      filename,
      content,
      type: 'component-doc'
    };
  }

  async generateBatch(components) {
    const docs = [];

    for (const component of components) {
      const doc = await this.generate(component);
      docs.push(doc);
    }

    // Generate index
    const index = this.generateIndex(components);
    docs.push(index);

    return docs;
  }

  prepareComponentData(component) {
    const {
      name,
      description = '',
      props = [],
      variants = [],
      examples = [],
      accessibility = '',
      designTokens = []
    } = component;

    return {
      name: this.formatPascalCase(name),
      description,
      packageName: this.options.packageName,
      language: this.options.language,
      usageExample: this.generateUsageExample(component),
      propsTable: this.generatePropsTable(props),
      variants: variants.map(v => ({
        name: v.name,
        description: v.description || '',
        example: this.generateVariantExample(component, v)
      })),
      accessibility: this.options.includeAccessibility ? accessibility : null,
      designTokens: designTokens.length > 0 ? designTokens : null,
      tokensTable: this.generateTokensTable(designTokens),
      examples: this.options.includeExamples ? examples.map(e => ({
        title: e.title || 'Example',
        description: e.description || '',
        code: e.code || this.generateExampleCode(component, e)
      })) : []
    };
  }

  generateUsageExample(component) {
    const name = this.formatPascalCase(component.name);
    const props = (component.props || [])
      .filter(p => p.required)
      .map(p => `${p.name}="${p.default || 'value'}"`)
      .join(' ');

    return `import { ${name} } from '${this.options.packageName}';

function Example() {
  return (
    <${name}${props ? ' ' + props : ''}>
      Content
    </${name}>
  );
}`;
  }

  generatePropsTable(props) {
    if (!props || props.length === 0) {
      return '| - | - | - | - | - |';
    }

    return props.map(prop => {
      const name = `\`${prop.name}\``;
      const type = `\`${prop.type || 'string'}\``;
      const defaultVal = prop.default !== undefined ? `\`${JSON.stringify(prop.default)}\`` : '-';
      const required = prop.required ? 'Yes' : 'No';
      const description = prop.description || '-';

      return `| ${name} | ${type} | ${defaultVal} | ${required} | ${description} |`;
    }).join('\n');
  }

  generateTokensTable(tokens) {
    if (!tokens || tokens.length === 0) return '';

    return tokens.map(token => {
      return `| \`${token.name}\` | \`${token.value}\` | ${token.description || '-'} |`;
    }).join('\n');
  }

  generateVariantExample(component, variant) {
    const name = this.formatPascalCase(component.name);
    return `<${name} variant="${variant.name}">
  ${variant.name} variant
</${name}>`;
  }

  generateExampleCode(component, example) {
    const name = this.formatPascalCase(component.name);
    const props = example.props
      ? Object.entries(example.props)
          .map(([k, v]) => `${k}={${JSON.stringify(v)}}`)
          .join(' ')
      : '';

    return `<${name}${props ? ' ' + props : ''}>
  ${example.children || 'Example content'}
</${name}>`;
  }

  generateIndex(components) {
    const links = components.map(c => {
      const name = this.formatPascalCase(c.name);
      const filename = this.formatKebabCase(c.name);
      return `- [${name}](./${filename}.md) - ${c.description || 'Component'}`;
    }).join('\n');

    return {
      filename: 'README.md',
      content: `# Components

${links}
`,
      type: 'index'
    };
  }

  formatPascalCase(name) {
    return name
      .replace(/[^a-zA-Z0-9]/g, ' ')
      .split(' ')
      .filter(Boolean)
      .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
      .join('');
  }

  formatKebabCase(name) {
    return name
      .replace(/[^a-zA-Z0-9]/g, '-')
      .toLowerCase()
      .replace(/-+/g, '-')
      .replace(/^-|-$/g, '');
  }
}

