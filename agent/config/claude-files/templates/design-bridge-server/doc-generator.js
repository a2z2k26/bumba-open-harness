class DocumentationGenerator extends EventEmitter {
  constructor(options = {}) {
    super();
    this.options = {
      outputDir: options.outputDir || './docs',
      format: options.format || 'markdown',
      ...options
    };

    this.componentGen = new ComponentDocGenerator(options);
    this.tokenGen = new TokenDocGenerator(options);
    this.apiGen = new APIDocGenerator(options);
    this.changelogGen = new ChangelogGenerator(options);
    this.siteGen = new DocSiteGenerator(options);

    // Forward events
    [this.componentGen, this.tokenGen, this.apiGen, this.changelogGen, this.siteGen].forEach(gen => {
      gen.on('doc:generated', (data) => this.emit('doc:generated', data));
    });
  }

  async generateComponentDocs(components) {
    return this.componentGen.generateBatch(components);
  }

  async generateTokenDocs(tokens) {
    return this.tokenGen.generate(tokens);
  }

  async generateAPIDocs(filePath) {
    return this.apiGen.generateFromFile(filePath);
  }

  async generateChangelog(releases) {
    return this.changelogGen.generate(releases);
  }

  async generateSite(config) {
    return this.siteGen.generate(config);
  }

  async generateAll(config) {
    const allDocs = [];

    if (config.components) {
      const componentDocs = await this.generateComponentDocs(config.components);
      allDocs.push(...componentDocs);
    }

    if (config.tokens) {
      const tokenDocs = await this.generateTokenDocs(config.tokens);
      allDocs.push(tokenDocs);
    }

    if (config.apiFiles) {
      for (const file of config.apiFiles) {
        const apiDoc = await this.generateAPIDocs(file);
        allDocs.push(apiDoc);
      }
    }

    if (config.releases) {
      const changelog = await this.generateChangelog(config.releases);
      allDocs.push(changelog);
    }

    this.emit('generation:complete', { totalDocs: allDocs.length });

    return allDocs;
  }

  async writeFiles(docs, outputDir) {
    outputDir = outputDir || this.options.outputDir;

    await fs.mkdir(outputDir, { recursive: true });

    for (const doc of docs) {
      const filePath = path.join(outputDir, doc.filename);
      const dir = path.dirname(filePath);

      await fs.mkdir(dir, { recursive: true });
      await fs.writeFile(filePath, doc.content, 'utf8');

      this.emit('file:written', { path: filePath });
    }

    return docs.length;
  }
}

// Factory functions
function createDocGenerator(type, options = {}) {
  const generators = {
    component: ComponentDocGenerator,
    token: TokenDocGenerator,
    api: APIDocGenerator,
    changelog: ChangelogGenerator,
    site: DocSiteGenerator,
    all: DocumentationGenerator
  };

  const Generator = generators[type];
  if (!Generator) {
    throw new Error(`Unknown documentation generator type: ${type}`);
  }

  return new Generator(options);
}

