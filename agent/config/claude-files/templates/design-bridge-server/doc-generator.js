class TokenDocGenerator extends EventEmitter {
  constructor(options = {}) {
    super();
    this.options = {
      outputDir: options.outputDir || './docs/tokens',
      format: options.format || 'markdown',
      name: options.name || 'Design Tokens',
      version: options.version || '1.0.0',
      ...options
    };
    this.templateEngine = new TemplateEngine();
  }

  async generate(tokens) {
    const docData = this.prepareTokenData(tokens);
    const template = DOC_TEMPLATES.token[this.options.format] || DOC_TEMPLATES.token.markdown;
    const content = this.templateEngine.render(template, docData);

    const extensions = {
      markdown: 'md',
      css: 'css',
      json: 'json'
    };
    const ext = extensions[this.options.format] || 'md';

    this.emit('doc:generated', { type: 'tokens', format: this.options.format });

    return {
      filename: `tokens.${ext}`,
      content,
      type: 'token-doc'
    };
  }

  prepareTokenData(tokens) {
    const categorized = this.categorizeTokens(tokens);

    return {
      name: this.options.name,
      description: this.options.description || 'Design system tokens',
      version: this.options.version,
      timestamp: new Date().toISOString(),
      colors: this.formatColors(categorized.colors || []),
      typography: this.formatTypography(categorized.typography || []),
      spacing: this.formatSimpleTokens(categorized.spacing || []),
      shadows: this.formatSimpleTokens(categorized.shadows || []),
      borderRadius: this.formatSimpleTokens(categorized.borderRadius || []),
      tokens: this.formatCSSTokens(tokens),
      categories: this.formatCategories(categorized)
    };
  }

  categorizeTokens(tokens) {
    const categories = {
      colors: [],
      typography: [],
      spacing: [],
      shadows: [],
      borderRadius: [],
      other: []
    };

    for (const token of tokens) {
      const category = this.detectCategory(token);
      if (categories[category]) {
        categories[category].push(token);
      } else {
        categories.other.push(token);
      }
    }

    return categories;
  }

  detectCategory(token) {
    const name = token.name.toLowerCase();

    if (name.includes('color') || name.includes('background') || name.includes('border-color') || name.includes('text-color')) {
      return 'colors';
    }
    if (name.includes('font') || name.includes('line-height') || name.includes('letter-spacing') || name.includes('text')) {
      return 'typography';
    }
    if (name.includes('spacing') || name.includes('margin') || name.includes('padding') || name.includes('gap')) {
      return 'spacing';
    }
    if (name.includes('shadow') || name.includes('elevation')) {
      return 'shadows';
    }
    if (name.includes('radius') || name.includes('rounded')) {
      return 'borderRadius';
    }

    return 'other';
  }

  formatColors(colors) {
    const grouped = this.groupByPrefix(colors);

    return Object.entries(grouped).map(([category, tokens]) => ({
      category,
      tokens: tokens.map(t => ({
        name: t.name,
        value: t.value,
        hexValue: this.extractHexColor(t.value)
      }))
    }));
  }

  formatTypography(typography) {
    const grouped = this.groupByPrefix(typography);

    return Object.entries(grouped).map(([category, tokens]) => ({
      category,
      tokens: tokens.map(t => ({
        name: t.name,
        value: t.value
      }))
    }));
  }

  formatSimpleTokens(tokens) {
    return tokens.map(t => ({
      name: t.name,
      value: t.value
    }));
  }

  formatCSSTokens(tokens) {
    const grouped = this.groupByPrefix(tokens);

    return Object.entries(grouped).map(([category, categoryTokens]) => ({
      category,
      values: categoryTokens.map(t => ({
        name: `--${t.name}`,
        value: t.value
      }))
    }));
  }

  formatCategories(categorized) {
    return Object.entries(categorized)
      .filter(([_, tokens]) => tokens.length > 0)
      .map(([name, tokens]) => ({
        name,
        tokens: tokens.map(t => ({
          name: t.name,
          value: t.value,
          type: this.detectTokenType(t),
          description: t.description
        }))
      }));
  }

  groupByPrefix(tokens) {
    const groups = {};

    for (const token of tokens) {
      const parts = token.name.split('-');
      const prefix = parts[0] || 'other';

      if (!groups[prefix]) {
        groups[prefix] = [];
      }
      groups[prefix].push(token);
    }

    return groups;
  }

  extractHexColor(value) {
    // Handle hex colors
    const hexMatch = value.match(/#([a-fA-F0-9]{6}|[a-fA-F0-9]{3})/);
    if (hexMatch) {
      return hexMatch[1];
    }

    // Handle rgb/rgba
    const rgbMatch = value.match(/rgba?\((\d+),\s*(\d+),\s*(\d+)/);
    if (rgbMatch) {
      const r = parseInt(rgbMatch[1]).toString(16).padStart(2, '0');
      const g = parseInt(rgbMatch[2]).toString(16).padStart(2, '0');
      const b = parseInt(rgbMatch[3]).toString(16).padStart(2, '0');
      return `${r}${g}${b}`;
    }

    return 'cccccc'; // Default gray for preview
  }

  detectTokenType(token) {
    const value = token.value;

    if (value.match(/^#|^rgb|^hsl/)) return 'color';
    if (value.match(/px$|rem$|em$|%$/)) return 'dimension';
    if (value.match(/^\d+$/)) return 'number';
    if (value.match(/^-?\d+(\.\d+)?$/)) return 'number';

    return 'string';
  }
}

