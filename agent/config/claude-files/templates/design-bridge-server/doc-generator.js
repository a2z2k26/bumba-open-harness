class ChangelogGenerator extends EventEmitter {
  constructor(options = {}) {
    super();
    this.options = {
      outputDir: options.outputDir || './',
      format: options.format || 'markdown',
      ...options
    };
    this.templateEngine = new TemplateEngine();
  }

  async generate(releases) {
    const template = DOC_TEMPLATES.changelog.markdown;
    const content = this.templateEngine.render(template, {
      releases: releases.map(r => ({
        ...r,
        date: r.date || new Date().toISOString().split('T')[0]
      }))
    });

    this.emit('doc:generated', { type: 'changelog' });

    return {
      filename: 'CHANGELOG.md',
      content,
      type: 'changelog'
    };
  }

  async generateFromCommits(commits, version) {
    const changes = this.categorizeCommits(commits);

    return this.generate([{
      version,
      date: new Date().toISOString().split('T')[0],
      ...changes
    }]);
  }

  categorizeCommits(commits) {
    const categories = {
      added: [],
      changed: [],
      deprecated: [],
      removed: [],
      fixed: [],
      security: []
    };

    for (const commit of commits) {
      const message = commit.message || commit;
      const category = this.detectCommitCategory(message);
      const cleanMessage = this.cleanCommitMessage(message);

      if (categories[category]) {
        categories[category].push(cleanMessage);
      }
    }

    // Remove empty categories
    Object.keys(categories).forEach(key => {
      if (categories[key].length === 0) {
        delete categories[key];
      }
    });

    return categories;
  }

  detectCommitCategory(message) {
    const lower = message.toLowerCase();

    if (lower.startsWith('feat') || lower.startsWith('add')) return 'added';
    if (lower.startsWith('fix') || lower.startsWith('bug')) return 'fixed';
    if (lower.startsWith('change') || lower.startsWith('update') || lower.startsWith('refactor')) return 'changed';
    if (lower.startsWith('deprecate')) return 'deprecated';
    if (lower.startsWith('remove') || lower.startsWith('delete')) return 'removed';
    if (lower.startsWith('security') || lower.startsWith('vuln')) return 'security';

    return 'changed';
  }

  cleanCommitMessage(message) {
    return message
      .replace(/^(feat|fix|chore|docs|style|refactor|test|build|ci|perf|revert)(\(.+\))?:\s*/i, '')
      .replace(/^[\w-]+:\s*/, '')
      .trim();
  }
}

