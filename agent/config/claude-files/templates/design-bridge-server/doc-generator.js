// ============================================================================
// Template Engine
// ============================================================================

class TemplateEngine {
  constructor() {
    this.helpers = new Map();
    this.registerDefaultHelpers();
  }

  registerDefaultHelpers() {
    this.registerHelper('pascalCase', (str) => {
      return str
        .replace(/[^a-zA-Z0-9]/g, ' ')
        .split(' ')
        .filter(Boolean)
        .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
        .join('');
    });

    this.registerHelper('camelCase', (str) => {
      const pascal = this.helpers.get('pascalCase')(str);
      return pascal.charAt(0).toLowerCase() + pascal.slice(1);
    });

    this.registerHelper('kebabCase', (str) => {
      return str
        .replace(/[^a-zA-Z0-9]/g, '-')
        .toLowerCase()
        .replace(/-+/g, '-')
        .replace(/^-|-$/g, '');
    });

    this.registerHelper('uppercase', (str) => str.toUpperCase());
    this.registerHelper('lowercase', (str) => str.toLowerCase());

    this.registerHelper('json', (obj) => JSON.stringify(obj, null, 2));

    this.registerHelper('date', (date, format) => {
      const d = date ? new Date(date) : new Date();
      if (format === 'iso') return d.toISOString();
      if (format === 'date') return d.toISOString().split('T')[0];
      return d.toLocaleDateString();
    });

    this.registerHelper('anchor', (str) => {
      return str.toLowerCase().replace(/[^a-z0-9]+/g, '-');
    });
  }

  registerHelper(name, fn) {
    this.helpers.set(name, fn);
  }

  render(template, data) {
    let result = template;

    // Handle helpers in format: {{helperName value}}
    result = result.replace(/\{\{(\w+)\s+(\w+)\}\}/g, (match, helper, key) => {
      const helperFn = this.helpers.get(helper);
      const value = this.getValue(data, key);
      if (helperFn && value !== undefined) {
        return helperFn(value);
      }
      return match;
    });

    // Handle conditionals: {{#if condition}}...{{/if}}
    result = this.processConditionals(result, data);

    // Handle loops: {{#each items}}...{{/each}}
    result = this.processLoops(result, data);

    // Handle simple variables: {{variable}}
    result = result.replace(/\{\{([^#/}]+)\}\}/g, (match, key) => {
      const value = this.getValue(data, key.trim());
      return value !== undefined ? String(value) : '';
    });

    // Clean up empty lines
    result = result.replace(/\n{3,}/g, '\n\n');

    return result;
  }

  processConditionals(template, data) {
    const ifRegex = /\{\{#if\s+(\w+)\}\}([\s\S]*?)\{\{\/if\}\}/g;

    return template.replace(ifRegex, (match, condition, content) => {
      const value = this.getValue(data, condition);
      if (value && (Array.isArray(value) ? value.length > 0 : true)) {
        return this.render(content, data);
      }
      return '';
    });
  }

  processLoops(template, data) {
    const eachRegex = /\{\{#each\s+(\w+)\}\}([\s\S]*?)\{\{\/each\}\}/g;

    return template.replace(eachRegex, (match, key, content) => {
      const items = this.getValue(data, key);
      if (!Array.isArray(items)) return '';

      return items.map((item, index) => {
        const itemData = {
          ...data,
          ...item,
          '@index': index,
          '@first': index === 0,
          '@last': index === items.length - 1
        };
        return this.render(content, itemData);
      }).join('');
    });
  }

  getValue(data, path) {
    const parts = path.split('.');
    let value = data;

    for (const part of parts) {
      if (value === undefined || value === null) return undefined;
      value = value[part];
    }

    return value;
  }
}

